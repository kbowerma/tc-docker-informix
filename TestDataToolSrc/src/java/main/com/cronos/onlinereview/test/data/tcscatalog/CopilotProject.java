package com.cronos.onlinereview.test.data.tcscatalog;

import java.io.Serializable;

/**
 * <p>A DTO for single copilot profile to be generated. Corresponds to <code>tcs_catalog.copilot_profile</code> database 
 * table.</p>
 * 
 * @author isv
 * @version 1.0
 */
public class CopilotProject implements Serializable {

    /**
     * <p>A <code>long</code> providing the ID of this project.</p>
     */
    private long copilotProjectId;

    /**
     * <p>A <code>long</code> providing the ID of TC Direct project.</p>
     */
    private long tcDirectProjectId;

    /**
     * <p>A <code>long</code> providing the ID for associated copilot profile.</p>
     */
    private long copilotProfileId;

    /**
     * <p>Constructs new <code>CopilotProject</code> instance. This implementation does nothing.</p>
     */
    public CopilotProject() {
    }

    /**
     * <p>Gets the ID for associated copilot profile.</p>
     *
     * @return a <code>long</code> providing the ID for associated copilot profile.
     */
    public long getCopilotProfileId() {
        return this.copilotProfileId;
    }

    /**
     * <p>Sets the ID for associated copilot profile.</p>
     *
     * @param copilotProfileId a <code>long</code> providing the ID for associated copilot profile.
     */
    public void setCopilotProfileId(long copilotProfileId) {
        this.copilotProfileId = copilotProfileId;
    }

    /**
     * <p>Gets the ID of TC Direct project.</p>
     *
     * @return a <code>long</code> providing the ID of TC Direct project.
     */
    public long getTcDirectProjectId() {
        return this.tcDirectProjectId;
    }

    /**
     * <p>Sets the ID of TC Direct project.</p>
     *
     * @param tcDirectProjectId a <code>long</code> providing the ID of TC Direct project.
     */
    public void setTcDirectProjectId(long tcDirectProjectId) {
        this.tcDirectProjectId = tcDirectProjectId;
    }

    /**
     * <p>Gets the ID of this project.</p>
     *
     * @return a <code>long</code> providing the ID of this project.
     */
    public long getCopilotProjectId() {
        return this.copilotProjectId;
    }

    /**
     * <p>Sets the ID of this project.</p>
     *
     * @param copilotProjectId a <code>long</code> providing the ID of this project.
     */
    public void setCopilotProjectId(long copilotProjectId) {
        this.copilotProjectId = copilotProjectId;
    }
}
